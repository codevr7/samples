grade = eval(input("enter your number grade (0 - 100):"))
if grade >= 90 :
    print("you got an A! amazing work")
elif grade >= 80 :
    print("you got a B . good  job ")
elif grade >= 70 :
    print("you got a C . good enough")
elif grade >= 60 :
    print("you got a D . you can do better")
else :
    print("you failed. try harder")
